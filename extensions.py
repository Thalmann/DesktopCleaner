def get_textfile_extensions():
    return ".txt", ".doc", ".pdf"

def get_shortcut_extensions():
    return ".lnk", ".url"

def get_picture_extensions():
    return ".png", ".jpg", ".gif"

def get_video_extensions():
    return ".wmv", ".flv", ".mkv", ".mp4"

def get_music_extensions():
    return ".mp3", ".wav"

def get_system_extensions():
    return ".bat", ".jar", ".ini", ".rar", ".zip"
